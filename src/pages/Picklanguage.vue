        <div class="content">
            <card padding="10px 10px" class="mycard">
                <div class="card-content" slot="card-content">
                    <div class="lang" v-for="(item,index) in locales" :key="index" @click.stop="chose(item)">
                        <span class="label" v-text="item.label"></span>
                        <span class="icons">                       
                            <!-- <i class="iconfont icon-dot1" v-if="isChosed(item)">✔</i>
                            <i class="iconfont icon-dot" v-else>✔</i> -->
                            <i class="iconfont_style_color" v-if="isChosed(item)">✔</i>
                            <i class="iconfont_style" v-else>✔</i>
                        </span>
                    </div>
            
                </div>
            </card>
        </div>
        <div class="bottom_start" @click="to_guidepage">
            {{$t("Start")}}
        </div>
        
    </div>
  
</template>

<script>
import { LANGUAGES } from '@/locales'
import Toolbar from '@/components/Toolbar'
import Card from '@/components/Card'
import { mapState, mapActions} from 'vuex'

export default {
    data () {
        return {
                
                    title:'Language',
                    showbackicon: false,
                    showmenuicon: false,
                 
                }
            },
    computed:{
        ...mapState({
            account: state => state.accounts.selectedAccount,
            accountData: state => state.accounts.accountData,
            app: state => state.app
            }),
        locales ()  {
             return LANGUAGES
                    }

    },
    methods :{
        to_guidepage(){
            this.$router.push({name: 'Guidepage'})
        },
         ...mapActions({
                setLocale: 'setLocale'
                }),
            back(){
            this.$router.back()
            },
        isChosed(item){
            if(item.key === this.app.locale.key){
                return true
            }
            return false
            },
        chose(item){
            //切换语言
            this.setLocale(item)
                .then(()=>{
                this.$toasted.show(this.$t('SaveSuccess'))
                this.$i18n.locale = item.key
                
                })
                .catch(err=>{
                this.$toasted.error(this.$t('SaveFailed'))
                })
            },

    },
    components: {
        Toolbar,
        Card,
  }
   
}
</script>

