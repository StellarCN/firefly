    <div class="page">
    <toolbar :title="$t(title)" 
      :showmenuicon="showmenuicon" 
      :showbackicon="showbackicon"
      @goback="back"
      />
        <!-- <div>
            <ul v-for="item in languages" :key="item.language_chosed">
                <li v-text="item.language_chosed"></li>
            </ul>
            <div v-for="(item,index) in locales" :key="index">
